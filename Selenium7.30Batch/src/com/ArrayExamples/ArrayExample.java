package com.ArrayExamples;

public class ArrayExample {

	public static void main(String[] args)
	{
		// dataType [] ArrayName = new dataType[size]
		//    or
		// dataType ArrayName[] = new dataType[size]
		
		int a[]= new int[3];
		
		// index of an Array always starts with 0
		
		a[0]=10;
		a[1]=20;
		a[2]=30;
		
		for(int i=0;i<3;i++)
		{
		System.out.println(a[1]);
		}
		

	}

}
